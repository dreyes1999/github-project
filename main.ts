function diver () {
    PLayer_diver = sprites.create(assets.image`Diver`, SpriteKind.Player)
    scaling.scaleToPercent(PLayer_diver, 50, ScaleDirection.Uniformly, ScaleAnchor.Middle)
    controller.moveSprite(PLayer_diver, 50, 40)
    PLayer_diver.setStayInScreen(true)
    O2()
    diver_health()
}
